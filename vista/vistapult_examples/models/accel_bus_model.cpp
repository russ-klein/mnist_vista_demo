void accel_bus_pvt::bind()
{
  /* connect pv to entry ports */
  
  
  getPV()->code_and_data.bind(code_and_data_entry);
  getPV()->console.bind(console_entry);
  getPV()->conv_2d_slave.bind(conv_2d_slave_entry);
  getPV()->dense_slave.bind(dense_slave_entry);
  getPV()->shared_memory.bind(shared_memory_entry);
  cpu_master_entry.bind(getPV()->cpu_master); 
  conv_2d_master_entry.bind(getPV()->conv_2d_master); 
  dense_master_entry.bind(getPV()->dense_master);   

  /* set callbacks on external and entry ports */ 

  cpu_master.b_transport_cb.set(&cpu_master_entry, &cpu_master_entry_type::b_transport);
  cpu_master.transport_dbg_cb.set(&cpu_master_entry, &cpu_master_entry_type::transport_dbg);
  cpu_master.get_direct_mem_ptr_cb.set(&cpu_master_entry, &cpu_master_entry_type::get_direct_mem_ptr);
  
  conv_2d_master.b_transport_cb.set(&conv_2d_master_entry, &conv_2d_master_entry_type::b_transport);
  conv_2d_master.transport_dbg_cb.set(&conv_2d_master_entry, &conv_2d_master_entry_type::transport_dbg);
  conv_2d_master.get_direct_mem_ptr_cb.set(&conv_2d_master_entry, &conv_2d_master_entry_type::get_direct_mem_ptr);
  
  dense_master.b_transport_cb.set(&dense_master_entry, &dense_master_entry_type::b_transport);
  dense_master.transport_dbg_cb.set(&dense_master_entry, &dense_master_entry_type::transport_dbg);
  dense_master.get_direct_mem_ptr_cb.set(&dense_master_entry, &dense_master_entry_type::get_direct_mem_ptr);
  
  code_and_data_entry.b_transport_cb.set(this, &self_type::b_transport_outside_code_and_data);
  code_and_data_entry.transport_dbg_cb.set(this, &self_type::transport_dbg_outside_code_and_data);
  code_and_data_entry.get_direct_mem_ptr_cb.set(&code_and_data, &code_and_data_type::get_direct_mem_ptr);
  
  console_entry.b_transport_cb.set(this, &self_type::b_transport_outside_console);
  console_entry.transport_dbg_cb.set(this, &self_type::transport_dbg_outside_console);
  console_entry.get_direct_mem_ptr_cb.set(&console, &console_type::get_direct_mem_ptr);
  
  conv_2d_slave_entry.b_transport_cb.set(this, &self_type::b_transport_outside_conv_2d_slave);
  conv_2d_slave_entry.transport_dbg_cb.set(this, &self_type::transport_dbg_outside_conv_2d_slave);
  conv_2d_slave_entry.get_direct_mem_ptr_cb.set(&conv_2d_slave, &conv_2d_slave_type::get_direct_mem_ptr);
  
  dense_slave_entry.b_transport_cb.set(this, &self_type::b_transport_outside_dense_slave);
  dense_slave_entry.transport_dbg_cb.set(this, &self_type::transport_dbg_outside_dense_slave);
  dense_slave_entry.get_direct_mem_ptr_cb.set(&dense_slave, &dense_slave_type::get_direct_mem_ptr);
  
  shared_memory_entry.b_transport_cb.set(this, &self_type::b_transport_outside_shared_memory);
  shared_memory_entry.transport_dbg_cb.set(this, &self_type::transport_dbg_outside_shared_memory);
  shared_memory_entry.get_direct_mem_ptr_cb.set(&shared_memory, &shared_memory_type::get_direct_mem_ptr);
  
  
  /* connect t non blocking callbacks */

  code_and_data.nb_transport_bw_cb.set(this, &self_type::receive_nb_transport_code_and_data);
  getT()->set_port_cb(3, &m_code_and_data_nb_connector);
  
  console.nb_transport_bw_cb.set(this, &self_type::receive_nb_transport_console);
  getT()->set_port_cb(4, &m_console_nb_connector);
  
  conv_2d_slave.nb_transport_bw_cb.set(this, &self_type::receive_nb_transport_conv_2d_slave);
  getT()->set_port_cb(5, &m_conv_2d_slave_nb_connector);
  
  dense_slave.nb_transport_bw_cb.set(this, &self_type::receive_nb_transport_dense_slave);
  getT()->set_port_cb(6, &m_dense_slave_nb_connector);
  
  shared_memory.nb_transport_bw_cb.set(this, &self_type::receive_nb_transport_shared_memory);
  getT()->set_port_cb(7, &m_shared_memory_nb_connector);
  
  cpu_master.nb_transport_fw_cb.set(this, &self_type::receive_nb_transport_cpu_master);
  getT()->set_port_cb(0, &m_cpu_master_nb_connector);
  
  conv_2d_master.nb_transport_fw_cb.set(this, &self_type::receive_nb_transport_conv_2d_master);
  getT()->set_port_cb(1, &m_conv_2d_master_nb_connector);
  
  dense_master.nb_transport_fw_cb.set(this, &self_type::receive_nb_transport_dense_master);
  getT()->set_port_cb(2, &m_dense_master_nb_connector);
  
  cpu_master_entry.invalidate_direct_mem_ptr_cb.set(&cpu_master, &cpu_master_type::invalidate_direct_mem_ptr);

  conv_2d_master_entry.invalidate_direct_mem_ptr_cb.set(&conv_2d_master, &conv_2d_master_type::invalidate_direct_mem_ptr);

  dense_master_entry.invalidate_direct_mem_ptr_cb.set(&dense_master, &dense_master_type::invalidate_direct_mem_ptr);

  code_and_data.invalidate_direct_mem_ptr_cb.set(&code_and_data_entry, &code_and_data_entry_type::invalidate_direct_mem_ptr);
  
  console.invalidate_direct_mem_ptr_cb.set(&console_entry, &console_entry_type::invalidate_direct_mem_ptr);
  
  conv_2d_slave.invalidate_direct_mem_ptr_cb.set(&conv_2d_slave_entry, &conv_2d_slave_entry_type::invalidate_direct_mem_ptr);
  
  dense_slave.invalidate_direct_mem_ptr_cb.set(&dense_slave_entry, &dense_slave_entry_type::invalidate_direct_mem_ptr);
  
  shared_memory.invalidate_direct_mem_ptr_cb.set(&shared_memory_entry, &shared_memory_entry_type::invalidate_direct_mem_ptr);
  
  /* check mb_event policies */
  m_accel_bus_t->check_mb_events();
  
}




